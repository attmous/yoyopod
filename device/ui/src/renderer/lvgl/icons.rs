const fn cellular_signal_map(level: u8) -> [u8; 196] {
    let heights = [3usize, 5, 7, 9];
    let mut map = [0u8; 196];
    let mut bar = 0usize;
    while bar < 4 {
        let x = 1 + bar * 3;
        let height = heights[bar];
        let first_y = 13 - height;
        let alpha = if (bar as u8) < level { 0xFF } else { 0x30 };
        let mut y = first_y;
        while y < 13 {
            map[y * 14 + x] = alpha;
            map[y * 14 + x + 1] = alpha;
            y += 1;
        }
        bar += 1;
    }
    map
}

