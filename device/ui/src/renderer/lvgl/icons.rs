const fn downsample_56_to_30(source: &[u8; 3136]) -> [u8; 900] {
    let mut target = [0u8; 900];
    let mut y = 0;
    while y < 30 {
        let source_y = y * 56 / 30;
        let mut x = 0;
        while x < 30 {
            let source_x = x * 56 / 30;
            target[y * 30 + x] = source[source_y * 56 + source_x];
            x += 1;
        }
        y += 1;
    }
    target
}

const fn abs_i32(value: i32) -> i32 {
    if value < 0 {
        -value
    } else {
        value
    }
}

